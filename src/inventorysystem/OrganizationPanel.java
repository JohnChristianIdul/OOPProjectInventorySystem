    public void displayOrganization() {
        ResultSet rs = conn.displayOrganization(txtUsername.getText());
        try {
            while(rs.next()) {
                boolean status = rs.getBoolean("employeestatus");
                String empstatus;
                if(status) {
                    empstatus = "Active";
                } else {
                    empstatus = "Inactive";
                }
                Object data[] = {rs.getString("employeeUsername"),rs.getString("restrictionlevel"),empstatus};
                tbl.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(OrganizationPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

