    private void tblOrganizationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblOrganizationMouseClicked
        // TODO add your handling code here:
        int index=tblOrganization.getSelectedRow();
        String empUsername = (String) tblOrganization.getValueAt(index, 0);
        String restrictionlvl = (String) tblOrganization.getValueAt(index, 1);
        tfEmpUsername.setText(empUsername);
        cbRestrict.setSelectedItem(restrictionlvl);
        if(tblOrganization.getValueAt(index, 2)=="Active") {
            btnActive.setSelected(true);
        } else {
            btnInactive.setSelected(true);
        }
    }//GEN-LAST:event_tblOrganizationMouseClicked

    private void btnAddEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEmpActionPerformed
        // TODO add your handling code here:
        String empUsername = tfEmpUsername.getText();
        String reslvl = (String) cbRestrict.getSelectedItem();
        boolean active = false;
        if(btnActive.isSelected()) {
            active = true;
        } else if(btnInactive.isSelected()) {
            active = false;
        }
        if(empUsername.isEmpty() || empUsername.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter an employee username.");
        } else {
            Employee e = new Employee(txtUsername.getText(),empUsername, reslvl, active);
            int addStatus = conn.addEmployee(e);
            if(addStatus == 0) {
                JOptionPane.showMessageDialog(this, "Employee already exists.");
            }
            tbl.setRowCount(0);
            displayOrganization();
        }
        
    }//GEN-LAST:event_btnAddEmpActionPerformed

    private void btnUpdateEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateEmpActionPerformed
        // TODO add your handling code here:
        int index=tblOrganization.getSelectedRow();
        String empUsernamePrev = (String) tblOrganization.getValueAt(index, 0);
        String empUsernameNew = tfEmpUsername.getText();
        String reslvl = (String) cbRestrict.getSelectedItem();
        String prevstatus = (String) tblOrganization.getValueAt(index,2);
        boolean changeToInact = false;
        if(prevstatus.equalsIgnoreCase("Active")) {
            changeToInact = true;
        }
        System.out.println(reslvl);
        boolean active = false;
        if(btnActive.isSelected()) {
            active = true;
        } else if(btnInactive.isSelected()) {
            active = false;
        }
        Employee e = new Employee(txtUsername.getText(),empUsernameNew, reslvl, active);
        if(e.isStatus() == false && changeToInact) {
            int input = JOptionPane.showConfirmDialog(null, "This will remove inventory access for this employee. Do you still want to proceed?","Caution",JOptionPane.YES_NO_CANCEL_OPTION);
            if(input==0) conn.updateEmployee(e, empUsernamePrev, txtUsername.getText());
        }
        else {
            conn.updateEmployee(e, empUsernamePrev, txtUsername.getText());
        }
        tbl.setRowCount(0);
        displayOrganization();
    }//GEN-LAST:event_btnUpdateEmpActionPerformed

