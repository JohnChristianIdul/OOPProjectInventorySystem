    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            double amount = Double.parseDouble(tfAmount.getText());
            int p;
            if(period.equals("Monthly")) {
                p = Integer.parseInt(lblDays.getText().substring(0, 2));
            } else {
                p = Integer.parseInt(lblDays.getText().substring(0, 3));
            }
            conn.updateBalance(username, payable);
            Verification v = new Verification(username, amount, payable, "subscribe", p, lblStype.getText(), 0);
            conn.subscribe(v);
            JOptionPane.showMessageDialog(null, "Verifying...");
            this.dispose();
        } catch(NumberFormatException n) {
            lblMessage.setText("Please input a valid amount");
        } catch (IllegalArgumentException i) {
            JOptionPane.showMessageDialog(null, i.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

