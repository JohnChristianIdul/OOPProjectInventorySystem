    public MakePayment(String username, double payable, String period, String subscriptionType) {
        initComponents();
        conn = new Connect();
        NumberFormat n = NumberFormat.getInstance();
        n.setMaximumFractionDigits(2);
        n.setMinimumFractionDigits(2);  
        lblStype.setText(subscriptionType);
        lblPeriod.setText(period);
        double p;
        if(period.equals("Monthly")) {
            lblDays.setText("30 days");
            if(subscriptionType.equals("Basic")) {
                lblTotalAnnual.setText("(₱ " + n.format(payable) + " annually)");
            } else {
                p = payable * 12;
                lblTotalAnnual.setText("(₱ " + n.format(p) + " annually)");
            }
        } else {
            lblDays.setText("365 days");
            if(subscriptionType.equals("Basic")) {
                lblTotalAnnual.setText("(₱ " + n.format(payable) + " monthly)");
            } else {
                p = payable / 12;
                lblTotalAnnual.setText("(₱ " + n.format(p) + " monthly)");
            }
        }
        lblPayable.setText("₱ " + n.format(payable));
        lblTotal.setText(lblPayable.getText());
        this.username = username;
        this.payable = payable;
        this.period = period;
    }

